#pragma once
/**
 *@brief Функция подсчета суммы для двух вещественных чисел
 * @param a первое слагаемое
 * @param b второе слагаемое
 * @return сумма a и b
 */
float sum(float a, float b);

/**
 *@brief Функция подсчета разности для двух вещественных чисел
 * @param a уменьшаемое
 * @param b вычитаемое
 * @return разность a и b
 */
float difference(float a, float b);

/**
 *@brief Функция подсчета произведения для двух вещественных чисел
 * @param a первый множитель
 * @param b второй множитель
 * @return произведение a и b
 */
float multiply(float a, float b);

/**
 *@brief Функция деления двух вещественных чисел
 * @param a делимое
 * @param b делитель
 * @return частное a и b
 */
float division(float a, float b);